package locators;

import java.util.concurrent.TimeUnit;

import org.openqa.selenium.By;
import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;
import org.openqa.selenium.chrome.ChromeDriver;

import io.github.bonigarcia.wdm.WebDriverManager;

public class Locators_ClassName {

	public static WebDriver driver;
	
	public static void main(String[] args) {
		WebDriverManager.chromedriver().setup();

		driver = new ChromeDriver();
		driver.manage().window().maximize();
		driver.manage().timeouts().implicitlyWait(20, TimeUnit.SECONDS);
		
		
		driver.get("https://www.myntra.com/");
		
		//Finding Element by ClassName
		//Use this only if the unique class names are assigned to Elements and not dynamic
		WebElement searchBar = driver.findElement(By.className("desktop-searchBar"));
		
		searchBar.sendKeys("shoes");
	}
}
